export type Caricature = {
  id: string;
  label: string;
  emoji: string;
  photo?: string;
  /** Full 52-blendshape map captured from the reference photo via /extract. */
  extracted?: Record<string, number>;
  /** Subset of blendshape names that count toward the match score. */
  activeKeys?: string[];
  /** Hand-tuned values that win over `extracted` for the given key. */
  overrides?: Record<string, number>;
  /** Resolved scoring target. Computed from extracted+activeKeys+overrides if absent. */
  target: Record<string, number>;
  weights?: Record<string, number>;
  threshold?: number;
  /** Soft cap per face in ms; default 20000. On expiry, advance with a miss. */
  capMs?: number;
};

type RawCaricature = Omit<Caricature, 'target'> & { target?: Record<string, number> };

function resolveTarget(c: RawCaricature): Record<string, number> {
  if (c.target) return c.target;
  if (!c.extracted || !c.activeKeys) {
    throw new Error(
      `Caricature ${c.id} needs either target or (extracted + activeKeys)`,
    );
  }
  const out: Record<string, number> = {};
  for (const k of c.activeKeys) {
    const v = c.overrides?.[k] ?? c.extracted[k];
    if (v != null) out[k] = v;
  }
  return out;
}

function resolve(list: RawCaricature[]): Caricature[] {
  return list.map((c) => ({ ...c, target: resolveTarget(c) }));
}

